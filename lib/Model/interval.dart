  final int readySecond;
  final int cycleCount;
  final int cycleBreakSecond;
  final int roundCount;
  final int exerciseSecond;
  final int breakSecond;

  const Interval({
    this.readySecond = 0,
    this.cycleCount = 0,
    this.cycleBreakSecond = 0,
    this.roundCount = 0,
    this.exerciseSecond = 0,
    this.breakSecond = 0,
  });
}